fn extract_date_from_filename(filename: &str) -> Option<String> {
    DATE_PATTERN.find(filename).map(|m| m.as_str().to_string())
}

async fn handler(event: Request) -> Result<Response<Body>, Error> {
    let uri = event.uri().path();

    if let Some(caps) = PATH_PATTERN.captures(uri) {
        let year = &caps[1];
        let month = &caps[2];
        let day = &caps[3];

        let date = format!("{}-{}-{}", year, month, day);

        for (_, (slug, mapping_date)) in URL_MAPPINGS.iter() {
            if mapping_date == &date {
                return create_redirect_response(slug);
            }
        }
    }

    let path = uri.trim_start_matches('/');

    let clean_path = path
        .split('.')
        .next()
        .unwrap_or(path)
        .trim_end_matches('/')
        .to_string();

    if let Some((new_slug, _)) = URL_MAPPINGS.get(&clean_path) {
        return create_redirect_response(new_slug);
    }

    if let Some(date) = extract_date_from_filename(&clean_path) {
        for (_, (new_slug, mapping_date)) in URL_MAPPINGS.iter() {
            if mapping_date == &date {
                return create_redirect_response(new_slug);
            }
        }
    }

    create_redirect_response("")
}

fn create_redirect_response(slug: &str) -> Result<Response<Body>, Error> {
    Ok(Response::builder()
        .status(301)
        .header(
            "location",
            format!("https://mechanicalrock.io/blog/{}", slug),
        )
        .body(format!("Redirected to https://mechanicalrock.io/blog/{}", slug).into())
        .map_err(Box::new)?)
}

#[tokio::main]
async fn main() -> Result<(), Error> {
    tracing_subscriber::fmt::init();
    run(service_fn(handler)).await
}
