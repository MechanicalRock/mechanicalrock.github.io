    static ref DATE_PATTERN: Regex = Regex::new(r"^\d{4}-\d{2}-\d{2}").unwrap();
    static ref PATH_PATTERN: Regex =
        Regex::new(r"^/(\d{4})/(\d{2})/(\d{2})/([^/]+?)(?:\.html)?$").unwrap();
